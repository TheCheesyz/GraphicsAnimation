VkFormat GraphicsPipelineHandler::findSupportedFormat(const std::vector<VkFormat>& candidates, VkImageTiling tiling, VkFormatFeatureFlags features)
{
	for (VkFormat format : candidates) {
		VkFormatProperties props;
		vkGetPhysicalDeviceFormatProperties(vh->getPhysicalDevice(), format, &props);
	
		if (tiling == VK_IMAGE_TILING_LINEAR && (props.linearTilingFeatures & features) == features) {
			return format;
		}
		else if (tiling == VK_IMAGE_TILING_OPTIMAL && (props.optimalTilingFeatures & features) == features) {
			return format;
		}
	}

	throw std::runtime_error("Unable to find a suitable format.");
}

VkFormat GraphicsPipelineHandler::findDepthFormat()
{
	return findSupportedFormat({VK_FORMAT_D32_SFLOAT, VK_FORMAT_D32_SFLOAT_S8_UINT, VK_FORMAT_D24_UNORM_S8_UINT},VK_IMAGE_TILING_OPTIMAL,VK_FORMAT_FEATURE_DEPTH_STENCIL_ATTACHMENT_BIT);
}

bool GraphicsPipelineHandler::hasStencilComponent(VkFormat format)
{
	return format == VK_FORMAT_D32_SFLOAT_S8_UINT || format == VK_FORMAT_D24_UNORM_S8_UINT;
}

void GraphicsPipelineHandler::cleanup() {
	vkDestroySampler(vh->getDevice(), textureSampler, nullptr);
	vkDestroyImageView(vh->getDevice(), textureImageView, nullptr);

	vkDestroyImage(vh->getDevice(), textureImage, nullptr);
	vkFreeMemory(vh->getDevice(), textureImageMemory, nullptr);

	vkDestroyDescriptorSetLayout(vh->getDevice(), descriptorSetLayout, nullptr);

	vkDestroyBuffer(vh->getDevice(), indexBuffer, nullptr);
	vkFreeMemory(vh->getDevice(), indexBufferMemory, nullptr);

	vkDestroyBuffer(vh->getDevice(), vertexBuffer, nullptr);
	vkFreeMemory(vh->getDevice(), vertexBufferMemory, nullptr);
	
	vkDestroyCommandPool(vh->getDevice(), commandPool, nullptr);
}

std::vector<char> GraphicsPipelineHandler::readFile(const std::string& filename)
{
	std::ifstream file(filename, std::ios::ate | std::ios::binary);

	if (!file.is_open()) {
		throw std::runtime_error("File could not be opened.");
	}

	size_t fileSize = (size_t)file.tellg();
	std::vector<char> buffer(fileSize);

	file.seekg(0);
	file.read(buffer.data(), fileSize);

	file.close();
	return buffer;
}

